#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	unsigned char	*d;
	unsigned char	*s;
	size_t			i;

	d = (unsigned char*)dst;
	s = (unsigned char*)src;
	i = 0;
	while (i++ < n)
	{
		*d++ = *s;
		if (*s == (unsigned char)c)
			return ((dst + i));
		s++;
	}
	return (NULL);
}
