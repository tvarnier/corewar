#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	if (lst != NULL)
	{
		while (lst->next != NULL)
		{
			(*f)(lst);
			lst = lst->next;
		}
		if (lst != NULL)
			(*f)(lst);
	}
}
