#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*src;
	size_t			i;

	i = 0;
	src = (unsigned char*)s;
	while (i < n)
	{
		if (*src == (unsigned char)c)
			return ((void*)s + i);
		i++;
		src++;
	}
	return (NULL);
}
