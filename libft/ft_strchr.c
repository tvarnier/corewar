#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	chr;

	chr = (char)c;
	if (s == NULL)
		return (NULL);
	while (*s != chr && *s != '\0')
		s++;
	if (*s != chr)
		return (NULL);
	return ((char*)s);
}
